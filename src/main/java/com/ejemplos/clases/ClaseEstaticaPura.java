package com.ejemplos.clases;

// Clase con métodos estáticos tipo "Helper"
public class ClaseEstaticaPura {

    // atributo estático es compartido entre todas las instancias
    public static int numero = 2;

    // Método estático que suma dos números y devuelve su suma
    //acceso statico return nombre (tipo1 parametro1, tipo2 parametro 2){}
    public static int suma (int i, int j){
        return i+j;
    }
}
